package modelos;

/**
 * Clase que representa una figura geometrica
 * 
 * @version 1.0
 * 
 * @author Fernando
 */
public abstract class Figura {
	
	/**
	 * Devuelve el area de la figura
	 * 
	 * @return		el area de la figura, en metros cuadrados
	 */
	public abstract double area();
	
	/**
	 * Devuelve el perimetro de la figura
	 * 
	 * @return		el perimetro de la figura, en metros cuadrados
	 */
	public abstract double perimetro();

}
